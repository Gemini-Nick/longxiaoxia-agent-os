export function localizeSystemText(locale: LongclawLocale, value?: string | null): string {
  if (!value) return ''
  const text = String(value).trim()
  if (!text) return ''
  if (locale !== 'zh-CN') return text
  const exact = systemTextLabels[text.toLowerCase()]
  if (exact) return exact
  if (/fetch failed|failed to fetch/i.test(text)) return '连接失败'
  if (/not configured|not_connected/i.test(text)) return '未配置'
  return text
}
